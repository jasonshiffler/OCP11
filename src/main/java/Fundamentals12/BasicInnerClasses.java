package Fundamentals12;

public class BasicInnerClasses {

    //Inner classes can have all access modifiers and be abstract or final
    public abstract class publicInnerClass { }
    protected final class protectedInnerClass { }
    abstract class defaultInnerClass { }
    private final class privateInnerClass { }

    public static abstract class  publicStaticInnerClass { }
    protected static final class  protectedStaticInnerClass { }
    static abstract class  defaultStaticInnerClass { }
    private static final class privateStaticInnerClass { }


}
